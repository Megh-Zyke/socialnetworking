      <div id="signing-up-registering">
        <h2 style="font-size: 2rem; color: #f4f4f4;">1. Signing Up / Registering</h2>
        <p style="font-size: 1.25rem; text-align: justify;">
          To sign up, follow these steps:
        </p>
        <ol style="font-size: 1.25rem;">
          <li>Open the app.</li>
          <li>Click on the <strong>"Sign Up"</strong> button.</li>
          <li>Enter your <strong>Full Name, Email/Phone Number, and Password</strong>.</li>
          <li>Verify your account via <strong>email or SMS</strong>.</li>
          <li>Complete your profile by adding a <strong>profile picture</strong> and bio.</li>
          <li>Set your <strong>privacy preferences</strong> (optional).</li>
        </ol>
      </div>

      <div id="connecting-with-people">
        <h2 style="font-size: 2rem; color: #f4f4f4;">2. Connecting with People</h2>
        <p style="font-size: 1.25rem; text-align: justify;">
          Build your social circle by following these steps:
        </p>
        <ul style="font-size: 1.25rem; list-style-type: disc; padding-left: 20px;">
          <li>Use the <strong>search bar</strong> to find people by name, email, or username.</li>
          <li>Send connection requests by clicking the <strong>"Connect"</strong> button.</li>
          <li>Accept or decline requests in the <strong>Notifications</strong> tab.</li>
          <li>Check the <strong>Suggestions</strong> tab to find mutual connections.</li>
        </ul>
      </div>

      <div id="creating-posts">
        <h2 style="font-size: 2rem; color: #f4f4f4;">3. Creating Posts</h2>
        <p style="font-size: 1.25rem; text-align: justify;">
          Share your thoughts, images, or videos by following these steps:
        </p>
        <ol style="font-size: 1.25rem;">
          <li>Click the <strong>"Create Post"</strong> button on the home or profile page.</li>
          <li>Select your content type: <strong>Text, Image, or Video</strong>.</li>
          <li>Add captions, tags, and hashtags (optional).</li>
          <li>Choose your audience: <strong>Public, Friends, or Custom</strong>.</li>
          <li>Click <strong>"Post"</strong> to share.</li>
        </ol>
      </div>

      <div id="editing-or-deleting-posts">
        <h2 style="font-size: 2rem; color: #f4f4f4;">4. Editing or Deleting Posts</h2>
        <p style="font-size: 1.25rem; text-align: justify;">
          You can always come back to edit or delete your posts:
        </p>
        <ol style="font-size: 1.25rem;">
          <li>Go to your <strong>Profile</strong> or the specific post.</li>
          <li>Click the <strong>three dots (···)</strong> at the top right corner.</li>
          <li>Select <strong>"Edit"</strong> to modify or <strong>"Delete"</strong> to remove.</li>
        </ol>
      </div>

      <div id="engaging-with-posts">
        <h2 style="font-size: 2rem; color: #f4f4f4;">5. Engaging with Posts</h2>
        <p style="font-size: 1.25rem; text-align: justify;">
          Support or share content by:
        </p>
        <ul style="font-size: 1.25rem; list-style-type: disc; padding-left: 20px;">
          <li><strong>Like</strong> posts (tap the heart/thumbs-up icon).</li>
          <li><strong>Share</strong> posts to your feed or with connections.</li>
          <li><strong>Comment</strong> to share your thoughts.</li>

        </ul>
      </div>

